                <h5>Kegiatan di Ksatriaan Entrepreneur Indonesia (KEI) meliputi :</h5>

                <h6>1. Pendidikan dan Pelatihan</h6>
                <p style="text-align: justify;">Joglo Tumiyono menyelenggarakan pendidikan entrepreneur di bidang peternakan (sapi dan unggas) serta pertanian bagi generasi muda yang potensial, berpendidikan, cerdas, berkomitmen terhadap bidang usaha yang digelutinya, memiliki kemauan kuat untuk belajar dan berusaha agar mampu menciptakan usaha-usaha baru yang ditangani secara cerdas.Juga berkomitmen terhadap pendidikan dan pengembangan Sumber Daya Manusia, Joglo Tumiyono juga menyelenggarakan berbagai program pendidikan dan pelatihan untuk menambah keahlian SDM, terutama dari kalangan internal WMP Group dan masyarakat sekitar. Hiasan relief ukiran yang menghiasi hampir seluruh bangunan Joglo Tumiyono dan berisi berbagai peristiwa bersejarah dari masa kerajaan, pra kemerdekaan, masa perjuangan mempertahankan kemerdekaan hingga kondisi terkini dengan dihadirkannya tokoh-tokoh presiden RI dari masa ke masa. Berharap generasi muda belajar nilai-nilai kehidupan dari keteladanan para tokoh bangsa, para pahlawan, founding fathers dan raja-raja penguasa masa lalu yang mampu menyatukan wilayah nusantara dengan keyakinan teguh yang dipegangnya. 
</p>



<h6>2. Membangun Kemitraan</h6>
                <p style="text-align: justify;">Membangun kemitraan yang saling menguntungkan, untuk meningkatkan kesejahteraan petani, peternak dan UMKM. Ada kurang lebih 4 juta usia muda yang potensial dididik untuk menjadi petani, peternak dan pelaku UMKM yang akan mengolah produknya dengan system pertanian yang telah disempurnakan metodenya agar menghasilkan panen yang maksimal dan mengurangi potensi kegagalan panen atau kerugian. Sementara UMKM akan dibekali dengan kelahlian mengelola keuangan, asetnya dan menjaga cashflow serta metode pemasaran yang efektif.
Ke depan pengembangan kemitraan akan berjalan berdampingan dengan pertumbuhan perusahaan. Saat ini kegiatan kemitraan yang sudah berjalan didukung oleh dua anak perusahaan WMP yaitu PT. Pasir Tengah (PST) yang melaksankan Kemitraan Sapi dan PT. Widodo Makmur Unggas (WMU) yang melaksanakan kemitraan ayam. </p>
<h6>Kemitraan Sapi</h6>
                <p style="text-align: justify;">Kemitraan sapi potong PT Pasir Tengah dilaksanakan untuk membantu para peternak yang sering mengalami permasalahan dalam usahanya. Kendala yang sering dialami oleh para peternak pada saat melakukan usaha pemeliharaan sapi potong diantaranya :  kesulitan akses modal di lembaga keuangan (dikenakan bunga yang tinggi), tidak adanya komitmen,  transfer teknologi, resiko kegagalan perkawinan, resiko kematian, harga panen yang bergantung pada tengkulak, sapi yang tidak dibayarkan saat transaksi, dll.
. 
</p>
<p style="text-align: justify;">Kemitraan sapi potong PT Pasir Tengah dibentuk sebagai salah satu bentuk program Corporate Social Responsibility (CSR) yang bertujuan meningkatkan pendapatan dan  kesejahteraan peternak lokal. PT Pasir Tengah bekerjasama dengan beberapa stakeholder membantu mensukseskan program Kemitraan sapi potong dengan melibatkan :</p>. 

<p>1) Kelembagaan Peternak</p>

<p>2) Lembaga Pembiayaan/Perbankan</p>

<p>3) Perusahaan Asuransi</p>

<p>4) Pemerintah Daerah</p>

<p style="text-align: justify;">Peran serta beberapa stakeholder diharapkan mampu memberikan keuntungan dan kemudahan bagi peternak yang mengikuti program kemitraan tersebut.

Adapun tujuan Kemitraan adalah :</p>

<p>1) Peternak dibantu memperoleh akses permodalan di lembaga keuangan (berbunga rendah)</p>

<p>2) Peternak mendapatkan pendampingan dan transfer teknologi langsung dari tim  peternakan internal PT Pasir Tengah</p>

<p>3) Peternak memperoleh jaminan pakan kosentrat yang berkualitas tinggi</p>

<p>4) Peternak memperoleh perlindungan asuransi terhadap sapi yang dipelihara hingga 100% dari total nilai sapi</p>
<p>5) Peternak memperoleh jaminan pasar atas sapi potongnya</p>
<p>6) Peternak memperoleh harga sapi yang kompetitif dan bersaing di pasar</p>

<p style="text-align: justify;">Program ini pertama kali diresmikan pada tanggal 9 Juli 2017 di Wonogiri, Jawa Tengah dengan Mitra dari Kelembagaan Peternak yaitu Badan Usaha Milik Petani (BUMP) PT Pengayom Tani Sejagad dengan total sapi Mitra sejumlah 115 ekor. Pengembangan tahun 2018 dilakukan di daerah Cianjur, Garut, Boyolali dengan Koperasi Serba Usaha (KSU)  dan Asosiasi Sapi Potong Indonesia (ASPIN).

Tahun 2019 PT Pasir Tengah melakukan pengembangan di Karanganyar dan Daerah Istimewa Yogyakarta. Jumlah sapi yang didistribusikan dalam 3 tahun ini mencapai 1.105 ekor.

Beberapa lembaga keuangan negeri dan swasta telah berkomitmen bekerja sama sebagai penyedia modal kerja dan asuransi. Hingga saat ini lembaga pembiayaan telah berhasil memberikan fasilitas kredit kepada peternak binaan PT Pasir Tengah  sebesar 16,8 Milyar dalam kurun waktu 3 tahun dengan skema Kredit Usaha Rakyat (KUR) maupun komersial.

Asuransi ternak sapi menjadi salah satu elemen penting dalam mencapai keberhasilan usaha ternak sapi di peternakan rakyat, karena mampu memberi jaminan terhadap aset (sapi) yang dimiliki oleh peternak. Beberapa perusahaan asuransi telah menjadi mitra kerja kami.

Kemitraan ini menggunakan sistem transparasi data yang baik sehingga meningkatkan rasa saling percaya, dilengkapi analisa lapangan yang akurat oleh PT Pasir Tengah serta didukung oleh perbankan yang akan mejaga dan mengatur lalu lintas keuangan peternak sehingga arus kas berjalan lancar. Oleh karena itu kemitraan ini diharapkan mampu direplikasi didaerah lain sehingga menjadi salah satu contoh permodelan kemitraan yang baik serta aman dalam rangka menambah pendapatan dan menambah daya saing peternak demi keberlanjutan dunia usaha peternakan rakyat.</p>


<h6>Kemitraan Ayam</h6>
                <p style="text-align: justify;">Saat ini Program Kemitraan ayam sedang digodog kembali oleh manajemen perusahaan, Divisi Research and Development WMP, serta managemen KEI. Saat konsep telah matang dan siap dilaksankan akan kami sampaikan pada website ini. 
</p>

<h6>3. Inkubator Bisnis</h6>
                <p style="text-align: justify;">Ksatriaan Entrepreneur Indonesia juga memberikan pelayanan konsultasi dan pendampingan bisnis bagi entrepreneur muda yang memulai membangun usaha salah satunya dengan menghubungkan pada lembaga bank atau lembaga keuangan lainnya untuk mendapatkan fasilitas pembiayaan. Juga pihak-pihak terkait lainnya seperti supplier atau lembaga  swadaya lainnya untuk mengadakan kerjasama usaha. </p>
                
